package pojo;

/**
 *
 * @author Marwan Adel
 */
public class Move {

    private int i;
    private int j;

    public Move() {
        i = -1;
        j = -1;
    }

    /**
     * @return the i
     */
    public int getI() {
        return i;
    }

    /**
     * @param i the i to set
     */
    public void setI(int i) {
        this.i = i;
    }

    /**
     * @return the j
     */
    public int getJ() {
        return j;
    }

    /**
     * @param j the j to set
     */
    public void setJ(int j) {
        this.j = j;
    }

}
